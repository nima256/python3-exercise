# exercise for list
# that bring negative number and positive number for you


# this code print negative number in list
list = [-2 , 1 , 19 , -75 , -23 , -4 , 56]
for x in list:
    if x <= 0 :
        print(x)
# output ==> -2 , -75 , -23 , -4

# this code print positive number in list
list2 = [-2 , 1 , 19 , -75 , -23 , -4 , 56]
for x in list2:
    if x > 0 :
        print(x)